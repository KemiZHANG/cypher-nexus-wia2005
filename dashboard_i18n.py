def t(key, language):
    return UI_TEXT.get(language, UI_TEXT["English"]).get(key, UI_TEXT["English"].get(key, key))


def localized_part_info(part_number, language):
    info = dict(PART_INFO[part_number])
    info.update(PART_TRANSLATIONS.get(language, {}).get(part_number, {}))
    return info


def mission_story(part_number, language):
    return MISSION_STORY_TRANSLATIONS.get(language, {}).get(part_number, MISSION_STORIES[part_number])


def part_label(part_number, language="English"):
    info = localized_part_info(part_number, language)
    return f"{t('part_word', language)} {part_number}: {info['title']}"
